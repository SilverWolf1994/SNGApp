package com.brandon.chavez.android.app.sngapp.utilidades;

public class DatosPacienteHistoriales
{
    private String codigoHistorial, nombreHistorial, descripcionHistorial, fechaHistorial, horaHistorial, alturaHistorial, pesoHistorial, imcHistorial;

    public void setCodigoHistorial(String codigoHistorial) { this.codigoHistorial = codigoHistorial; }

    public void setNombreHistorial(String nombreHistorial) { this.nombreHistorial = nombreHistorial; }

    public void setDescripcionHistorial(String descripcionHistorial) { this.descripcionHistorial = descripcionHistorial; }

    public void setFechaHistorial(String fechaHistorial) { this.fechaHistorial = fechaHistorial; }

    public void setHoraHistorial(String horaHistorial) { this.horaHistorial = horaHistorial; }

    public void setAlturaHistorial(String alturaHistorial) { this.alturaHistorial = alturaHistorial; }

    public void setPesoHistorial(String pesoHistorial) { this.pesoHistorial = pesoHistorial; }

    public void setImcHistorial(String imcHistorial) { this.imcHistorial = imcHistorial; }

    public String getCodigoHistorial()
    {
        return codigoHistorial;
    }

    public String getNombreHistorial() { return nombreHistorial; }

    public String getDescripcionHistorial()
    {
        return descripcionHistorial;
    }

    public String getFechaHistorial()
    {
        return fechaHistorial;
    }

    public String getHoraHistorial()
    {
        return horaHistorial;
    }

    public String getAlturaHistorial()
    {
        return alturaHistorial;
    }

    public String getPesoHistorial()
    {
        return pesoHistorial;
    }

    public String getImcHistorial()
    {
        return imcHistorial;
    }
}
